/*-----------------------------------------------------------------------------
  Function:    PWM_Start                                                                                                                                                                            
  input:       period--周期 
               width--占空比配置
               div--预分频
               outRev--输出反向                        
  output:      None                                                                                    
  Returns:     None                                                                                      
  Description: 配置PWM的周期和占空比并启动                                                                                                                                     
 *-----------------------------------------------------------------------------*/
void PWM_Start(SGCC_PWM_TypeDef* pwmx, uint8_t period, uint8_t width, uint8_t div, uint8_t outRev) 
{
    pwmx->CONTROL = 0;
    pwmx->PERIOD = period;
    pwmx->WIDTH = width;
    pwmx->DIV = div;
    pwmx->CONTROL = (outRev << PWM_CONTROL_REVB) | 0x5;
}

